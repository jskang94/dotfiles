require('lspconfig').pyright.setup({
  on_attach = on_attach,
  capabilities = lsp_status.capabilities
})

require('lspconfig').rust_analyzer.setup({
  on_attach = on_attach,
  settings = {
    ["rust-analyzer"] = {
      imports = {
        granularity = {
          group = "module",
        },
        prefix = "self",
      },
      cargo = {
        buildScripts = {
          enable = true,
        },
      },
      procMacro = {
        enable = true
      },
    }
  }
})

return {
  on_attach = on_attach,
  lsp_status = lsp_status,
}
